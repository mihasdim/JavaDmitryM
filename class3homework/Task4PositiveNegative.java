package class3homework;
/* Write a program to check whether number is positive or negative.*/
public class Task4PositiveNegative {
    public static void main(String[] args) {

        double num=5;

        if (num>0) {
            System.out.println("Number "+num+ " is positive");

        }else if (num<0) {
            System.out.println("Number " + num + " is negative");

        }else if (num == 0) {
            System.out.println("Enter any number which differs from zero!!!");

        }



    }

}
