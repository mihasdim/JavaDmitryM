package class3;

public class RelationalOperators_6 {
    public static void main(String[] args) {
        /* Arithmetic operators
        int(+,-,*,/)int=int

        Relational operators
        >, =>, !=, =<
        true or false ONLY

         */
        int number=10;
        int number2=10;
        System.out.println(number+number2);
        boolean result=20>10;
        System.out.println(result);
        System.out.println(20>10); // is 20 greater than 10 - true
        System.out.println(5>10); // is 5 greater than 10 - false
        System.out.println(5!=10); // is 5  not equal to 10 - false symbol != means not equal
        System.out.println(5==10); // is 5  not equal to 10 - false
        System.out.println(5==5); // is 5  not equal to 10 - true

    }
}
