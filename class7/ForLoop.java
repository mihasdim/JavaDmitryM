package class7;

public class ForLoop {
    public static void main(String[] args) {
        /*
        prints numbers from 0 to 9
        */
        int number = 0;
        while (number < 10) {
            System.out.println(number);
            number++;
        }


         /*(initialize ; condition ; incrementORdecrement)*/


        for (int i=0; number<10;i++) {
            System.out.println(i);

    }







    }
}
