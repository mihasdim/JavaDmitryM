        box1=100;
        short box2=-31000;
        box2=15000;
        int box3=123456789;
        box3=5000000;
        long box4=98765432123456789l;
        box4=30000000000000l;
        float box5=555.333f;
        box5=111.777f;
        double box6=333.5557773330005;
        box6=777.999777999333;
